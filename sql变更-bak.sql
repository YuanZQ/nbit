

CREATE TABLE `dict` (
  `id` varchar(40) NOT NULL,
  `name` varchar(200) DEFAULT NULL COMMENT '名称',
  `type_id` varchar(100) DEFAULT NULL COMMENT '类型',
  `code` varchar(40) DEFAULT NULL COMMENT '编码',
  `seq` int(3) DEFAULT NULL COMMENT '顺序',
  `description` varchar(1024) DEFAULT NULL COMMENT '描述',
  `delete_flag` varchar(1) DEFAULT NULL COMMENT '删除状态，0表示正常，1表示删除',
  `create_user_id` varchar(40) DEFAULT NULL COMMENT '创建人',
  `create_time` datetime DEFAULT NULL COMMENT '创建时间',
  `last_modify_user_id` varchar(40) DEFAULT NULL COMMENT '最后修改人',
  `last_modify_time` datetime DEFAULT NULL COMMENT '最后修改时间',
  PRIMARY KEY (`id`),
  KEY `INX_SDICT_CODE` (`code`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='字典表';


